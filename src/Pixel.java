public class Pixel {
    int r;
    int g;
    int b;
    int x;
    int y;
    int l;
    int w;

    public Pixel(int r, int g, int b, int x, int y,int w, int l) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.x = x;
        this.y = y;
        this.l = l;
        this.w = w;
    }
}
